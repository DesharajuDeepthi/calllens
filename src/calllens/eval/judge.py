"""
LLM-as-judge for insight quality.

Asks GPT to score whether a generated insight is:
  1. Relevant to the persona that receives it
  2. Supported by the evidence call IDs listed
  3. Actionable (contains a clear implication or next step)

Score: 0.0 – 1.0 per dimension; overall = mean of all three.
"""

from __future__ import annotations

import json

from pydantic import BaseModel, Field

from calllens.llm.provider import get_llm


class InsightScore(BaseModel):
    relevance: float = Field(ge=0.0, le=1.0, description="0=irrelevant, 1=highly relevant")
    specificity: float = Field(ge=0.0, le=1.0, description="0=vague, 1=specific/concrete")
    actionability: float = Field(ge=0.0, le=1.0, description="0=no action implied, 1=clear action")
    reasoning: str = Field(description="One sentence explanation of the scores")

    @property
    def overall(self) -> float:
        return (self.relevance + self.specificity + self.actionability) / 3


_JUDGE_PROMPT = """\
You are evaluating the quality of a business insight generated by an AI analytics platform.

Persona this insight is for: {persona}
Insight type: {insight_type}
Severity: {severity}
Title: {title}
Body: {body}
Evidence call IDs (count): {evidence_count}

Rate the insight on three dimensions from 0.0 to 1.0:
- relevance: Is this insight relevant and useful for a {persona}?
- specificity: Is the insight specific and grounded (not just vague advice)?
- actionability: Does the insight imply a clear next step or decision?

Return a JSON object with keys: relevance, specificity, actionability, reasoning.
reasoning must be a single sentence.
"""


async def judge_insight(insight: dict) -> InsightScore:
    """Score a single insight dict using LLM-as-judge."""
    llm = get_llm(temperature=0.0)
    structured = llm.with_structured_output(InsightScore)

    prompt = _JUDGE_PROMPT.format(
        persona=insight.get("persona", "unknown"),
        insight_type=insight.get("insight_type", "unknown"),
        severity=insight.get("severity", "medium"),
        title=insight.get("title", ""),
        body=(insight.get("body", "") or "")[:600],
        evidence_count=len(insight.get("evidence_call_ids") or []),
    )

    result = await structured.ainvoke(prompt)
    return result


async def judge_insights_sample(
    insights: list[dict],
    sample_size: int = 5,
) -> dict:
    """
    Judge a random sample of insights.
    Returns aggregated scores and per-insight results.
    """
    import random

    sample = random.sample(insights, min(sample_size, len(insights)))

    results = []
    for ins in sample:
        try:
            score = await judge_insight(ins)
            results.append({
                "persona": ins.get("persona"),
                "insight_type": ins.get("insight_type"),
                "title": ins.get("title"),
                "relevance": score.relevance,
                "specificity": score.specificity,
                "actionability": score.actionability,
                "overall": score.overall,
                "reasoning": score.reasoning,
            })
        except Exception as exc:
            results.append({
                "persona": ins.get("persona"),
                "title": ins.get("title"),
                "error": str(exc),
                "overall": 0.0,
            })

    valid = [r for r in results if "error" not in r]
    avg_overall = sum(r["overall"] for r in valid) / len(valid) if valid else 0.0
    avg_relevance = sum(r["relevance"] for r in valid) / len(valid) if valid else 0.0
    avg_actionability = sum(r["actionability"] for r in valid) / len(valid) if valid else 0.0

    return {
        "sample_size": len(sample),
        "avg_overall": round(avg_overall, 3),
        "avg_relevance": round(avg_relevance, 3),
        "avg_actionability": round(avg_actionability, 3),
        "results": results,
    }
